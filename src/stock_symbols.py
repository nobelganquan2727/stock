def get_all_stock_codes() -> List[str]:
    """获取所有股票代码（去重）"""
    all_codes = set()
    all_codes.update(SZ50_CODES)
    all_codes.update(HS300_CORE_CODES)
    all_codes.update(ZZ500_CODES)
    return sorted(list(all_codes))


def get_etf_codes(category: str = None) -> List[str]:
    """获取ETF代码"""
    if category and category in ETF_LIST:
        return ETF_LIST[category]
    all_etfs = []
    for etfs in ETF_LIST.values():
        all_etfs.extend(etfs)
    return list(set(all_etfs))


